
public class CMS_Data {
	
	public CMS_Data(String firstName, String midName, String lastName, String cardNumber, String expMonth, String expYear, String svc){
		first_name = firstName;
		mid_name = midName;
		last_name = lastName;

		card_number = cardNumber;
		exp_month = expMonth;
		exp_year = expYear;
		service_code = svc;
	}
	
	
	public String first_name;
	public String mid_name;
	public String last_name;
	public String card_number;
	public String exp_month;
	public String exp_year;
	public String service_code;
}
